
/*
Comand for checking all the classes available 
in the respective datatypes (using the TERMINAL) : javap java.lang.Float
*/
public class four{
    public static void main(String[] args){
        System.out.println("Int Max Value: "+Integer.MAX_VALUE);
        System.out.println("Int Min Value: "+Integer.MIN_VALUE);
        System.out.println("Int Size: "+Integer.SIZE);
        System.out.println("Int Bytes: "+Integer.BYTES);

        System.out.println("BYTES Max Value: "+Byte.MAX_VALUE);
        System.out.println("BYTES Min Value: "+Byte.MIN_VALUE);
        System.out.println("BYTES Size: "+Byte.SIZE);
        System.out.println("Bytes: "+Byte.BYTES);

        System.out.println("Float Max Value: "+Float.MAX_VALUE);
        System.out.println("Float Min Value: "+Float.MIN_VALUE);
        System.out.println("Float Size: "+Float.SIZE);
        System.out.println("Float Bytes: "+Float.BYTES);

    }
}